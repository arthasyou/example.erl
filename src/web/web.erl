start() ->
    {ok, Port} = application:get_env(http, port),
    Dispatch = cowboy_router:compile([
        {'_', [
            {"/[...]", toppage_h, []}
        ]}
    ]),
    {ok, _} = cowboy:start_clear(http, [{port, Port}], #{
        env => #{dispatch => Dispatch}
    }).


